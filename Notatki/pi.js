// W JavaScript, wartość π (pi) może być uzyskana za pomocą właściwości Math.PI. Oto przykład tego, jak można jej użyć:

console.log(Math.PI);  // Output: 3.141592653589793
// Można również używać wartości π w wyrażeniach i operacjach matematycznych tak samo, jak każdej innej wartości liczbowej. Na przykład:

let promień = 5;
let obwód = 2 * Math.PI * promień
;
console.log(obwód);  // Output: 31.41592653589793
